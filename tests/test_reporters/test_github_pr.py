def test_build_comment_body_contains_marker(sample_drift_report):
    """Comment body starts with the marker so we can find it later."""
    reporter = GitHubPRReporter(sample_drift_report)
    body = reporter._build_comment_body()
    assert body.startswith(COMMENT_MARKER)


def test_build_comment_body_has_table(sample_drift_report):
    """Comment body contains a markdown table."""
    reporter = GitHubPRReporter(sample_drift_report)
    body = reporter._build_comment_body()
    lines = body.splitlines()
    # Find the header separator
    assert any(l.startswith("| Severity |") for l in lines)
    assert any(l.startswith("|----------|") for l in lines)


