@responses.activate
def test_report_github_pr_uses_github_output_detection(tmp_path, monkeypatch):
    """When repo/pr are not set but GITHUB_OUTPUT provides them, uses those."""
    github_output = tmp_path / "github_output.txt"
    github_output.write_text("pr_number=42\nrepo=my-org/my-repo\n")
    monkeypatch.setenv("GITHUB_OUTPUT", str(github_output))
    monkeypatch.setenv("GITHUB_TOKEN", "env-token")

    responses.add(
        responses.GET,
        "https://api.github.com/repos/my-org/my-repo/issues/42/comments",
        json=[],
        status=200,
    )
    responses.add(
        responses.POST,
        "https://api.github.com/repos/my-org/my-repo/issues/42/comments",
        json={"id": 1},
        status=201,
    )

    report = DriftReport(scanned_path=Path("."))
    reporter = GitHubPRReporter(report)
    success = reporter.report_github_pr()

    assert success is True
    assert reporter.pr_number == 42
    assert reporter.repo == "my-org/my-repo"
