# Tests: detect_pr_from_github_output
# ---------------------------------------------------------------------------


def test_detect_pr_from_github_output(tmp_path, monkeypatch):
    """Reads pr_number and repo from GITHUB_OUTPUT file."""
    github_output = tmp_path / "github_output.txt"
    github_output.write_text("pr_number=42\nrepo=my-org/my-repo\n")
    monkeypatch.setenv("GITHUB_OUTPUT", str(github_output))

    reporter = GitHubPRReporter(DriftReport(scanned_path=Path(".")))
    pr_number, repo = reporter.detect_pr_from_github_output()

    assert pr_number == 42
    assert repo == "my-org/my-repo"


def test_detect_pr_from_github_output_missing(monkeypatch):
    """Returns (None, None) when GITHUB_OUTPUT is not set."""
    monkeypatch.delenv("GITHUB_OUTPUT", raising=False)
    reporter = GitHubPRReporter(DriftReport(scanned_path=Path(".")))
    pr_number, repo = reporter.detect_pr_from_github_output()
    assert pr_number is None
    assert repo is None


